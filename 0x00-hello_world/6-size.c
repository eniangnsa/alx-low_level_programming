#include <stdio.h>
/**
 * this program prints sizes of type
 */ 
int main(void) /* main entry */
{
printf("the size of a char : %d\n", sizeof(char));
printf("the size of an int : %d\n", sizeof(int));
printf("the size of a long int : %d\n", sizeof(long int));
printf("the size of a long long int : %d\n", sizeof(long long int));
printf("the size of a float : %d\n", sizeof(float));
return (0);
}
